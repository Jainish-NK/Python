import math

print(math.exp(1))       # e^1 = 2.718281828459045
print(math.expm1(1))     # e^1 - 1 = 1.718...

print(math.log(10))      # Natural log (base e)
print(math.log10(100))   # Log base 10 → 2.0
print(math.log2(8))      # Log base 2 → 3.0


print(math.degrees(math.pi))   # 180.0
print(math.radians(180))       # 3.141592653589793


print(math.gcd(12, 18))     # 6 (Greatest Common Divisor)
print(math.lcm(4, 6))       # 12 (Least Common Multiple)
print(math.isqrt(10))       # 3 (Integer square root)
print(math.isfinite(10))    # True
print(math.isinf(math.inf)) # True
print(math.isnan(math.nan)) # True
